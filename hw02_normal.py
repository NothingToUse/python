if user_input_task == 1:
    print(' Задача - 1 - нормал')
    old = []
    new = []

    count = 0
    print('Чтобы выйти - введите 0')
    act_1 = int(input('Для пополнения массива введите 1'))
    while count >= 0:
        if act_1 == 1:
            x = input('Пополните массив: ')
            old.append(x)
            count += 1

            ans_3 = int(input('Пополнить массивы еще? Если да - введите 1, если достаточно - нажмите другую цифру'))

            if ans_3 == 1:
                print('Массив содержит следующие значения: ', old)
            else:
                print('Массив а содержит следующие элементы: ', old)
                print('Переходим к решению задачи...')
                for i in old[:]:
                    if int(i) > 0:
                        if math.sqrt(int(i)) > 0:
                            new_el = math.sqrt(int(i))
                            if new_el % new_el == 0:
                                if new_el / int(new_el) == 1:
                                    # отсекаем значения с запятой, например при извлечении
                                    # квадратного корня из 5 (в тестовых числах нет)
                                    new.append(int(new_el))
                print('Новый массив: ', new)
                break
        else:
            break
